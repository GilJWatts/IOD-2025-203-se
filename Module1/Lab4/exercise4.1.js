// Function to add two numbers
function add(num1, num2) {
    const sum = num1 + num2;
    return sum;
  }
  
  // Function to subtract two numbers
  function subtract(num1, num2) {
    const difference = num1 - num2;
    return difference;
  }
  
  // Function to multiply two numbers
  function multiply(num1, num2) {
    const product = num1 * num2;
    return product;
  }
  
  // Function to divide two numbers
  function divide(num1, num2) {
    const quotient = num1 / num2;
    return quotient;
  }
  
  // Example calls and output:
  const result1 = add(5, 3);
  console.log("5 + 3 = " + result1); // Output: 5 + 3 = 8
  
  const result2 = subtract(10, 4);
  console.log("10 - 4 = " + result2); // Output: 10 - 4 = 6
  
  const result3 = multiply(6, 7);
  console.log("6 * 7 = " + result3); // Output: 6 * 7 = 42
  
  const result4 = divide(20, 5);
  console.log("20 / 5 = " + result4); // Output: 20 / 5 = 4

  